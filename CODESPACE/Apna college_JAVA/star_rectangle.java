public class star_rectangle {
    public static void main(String[] args){

        int m=4;
        int n=5;
    
        //For outer loops
        for(int i=1; i<=m; i++){
            //For inner loops
            for(int j=1; j<=n; j++){
                System.out.print("*");
            }
                System.out.println();
        }
    }
}
